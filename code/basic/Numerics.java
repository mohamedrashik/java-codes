package code.basic;

public class Numerics {
    public static void main(String[] args) {
        int dec = 100;
        int bin = 0b101;
        int oct = 0100;
        int hex = 0x1A;

        System.out.println(dec);
        System.out.println(bin);
        System.out.println(oct);
        System.out.println(hex);

       // int max = 2147483647;
       // int max = 2147483648; out of range
        float f = 1.0e-2f;
       System.out.println(f);
        }
}
